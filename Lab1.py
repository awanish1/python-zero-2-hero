# Basic Python 3 program

# Print a greeting
print("Hello, World!")

# Perform basic arithmetic operations
a = 10
b = 5

# Addition
addition = a + b
print("Addition:", addition)

# Subtraction
subtraction = a - b
print("Subtraction:", subtraction)

# Multiplication
multiplication = a * b
print("Multiplication:", multiplication)

# Division
division = a / b
print("Division:", division)

# Modulus
modulus = a % b
print("Modulus:", modulus)
